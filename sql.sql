CREATE OR REPLACE FUNCTION obtener_tabla_con_parametro(tabla bigint)
  RETURNS TABLE (
n_campos bigint,
id_tabla  bigint,
nombre_tabla text,
numero_tabla bigint,
 id_campo bigint,
  nombre_campo text, 
  id_tabla_id bigint,
  id_valor bigint,
  valor text,
  activo boolean,
  linea bigint,
  id_campo_id bigint
  )
AS $$
BEGIN
  RETURN QUERY 
  select 
(select count(*) from core_tabla ct  
inner join core_campo_tabla cct  on cct.id_tabla_id = ct.id_tabla where ct.numero_tabla = tabla)
,

*
from core_tabla ct  
inner join core_campo_tabla cct  on cct.id_tabla_id = ct.id_tabla and ct.numero_tabla = tabla 
inner join core_valor_campo cvc  on cvc.id_campo_id = cct.id_campo
order by cvc.linea, cvc.id_valor;

  
END;
$$ LANGUAGE plpgsql;
